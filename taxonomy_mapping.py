def validate_benchmark(benchmark: list[dict]) -> list[str]:
    """
    Validate all taxonomy fields in a loaded benchmark_v2.json.

    Returns a list of error strings (empty if all valid).

    Usage:
        import json
        from taxonomy_mapping import validate_benchmark
        data = json.load(open("benchmark_v2.json", encoding="utf-8"))
        errors = validate_benchmark(data)
        for e in errors:
            print(e)
    """
    errors: list[str] = []
    for item in benchmark:
        eid = item.get("id", "UNKNOWN")
        for dim, known in _KNOWN_VALUES.items():
            val = item.get(dim)
            if val is None:
                errors.append(f"{eid}: missing field '{dim}'")
            elif val not in known:
                errors.append(
                    f"{eid}: '{dim}' has unknown value '{val}' "
                    f"(expected one of {sorted(known)})"
                )
    return errors


def low_variance_report(benchmark: list[dict]) -> dict[str, dict[str, int]]:
    """
    Compute value-frequency distributions for the four low-variance dimensions.

    Returns a dict: {dimension: {value: count}}

    Use this in the paper to justify why those 4 dimensions are metadata-only:
        report = low_variance_report(data)
        # e.g. report["authority_claimed"] == {"none": 82, "ai_company": 5, ...}
    """
    from collections import Counter
    result: dict[str, dict[str, int]] = {}
    for dim in LOW_VARIANCE_DIMS:
        counts = Counter(item.get(dim, "MISSING") for item in benchmark)
        result[dim] = dict(counts.most_common())
    return result


def high_variance_report(benchmark: list[dict]) -> dict[str, dict[str, int]]:
    """Same as low_variance_report but for the 4 analysis dimensions."""
    from collections import Counter
    result: dict[str, dict[str, int]] = {}
    for dim in HIGH_VARIANCE_DIMS:
        counts = Counter(item.get(dim, "MISSING") for item in benchmark)
        result[dim] = dict(counts.most_common())
    return result
