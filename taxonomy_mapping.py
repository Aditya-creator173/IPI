"""
taxonomy_mapping.py
===================
Defines the authoritative 4-dimension analysis taxonomy for IPIBench.

The benchmark_v2.json contains 8 classification dimensions. Four of them
(authority_claimed, target_action_type, linguistic_register, harm_severity)
were auto-generated by migrate_benchmark.py using narrow keyword heuristics
and exhibit low variance across scenarios -- they default to the same values
in the majority of cases and are not suitable as primary analysis axes.

This file:
  1. Defines which 4 dimensions are HIGH-VARIANCE (used in paper analysis)
  2. Defines which 4 dimensions are LOW-VARIANCE (kept as metadata only)
  3. Provides label maps and ordering for the high-variance dimensions
  4. Provides validation utilities used by the analysis notebook

Usage in analysis.ipynb:
  from taxonomy_mapping import HIGH_VARIANCE_DIMS, ANALYSIS_DIMS, DIM_LABELS
"""

